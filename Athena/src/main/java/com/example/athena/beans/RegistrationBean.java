package com.example.athena.beans;

public class RegistrationBean {
    private String email ;
    private String code ;

    public void setEmail(String email) {
        this.email = email ;
    }

    public String getEmail() {
        return this.email ;
    }

    public void setCode(String code) {
        this.code = code ;
    }

    public String getCode() {
        return this.code ;
    }
}
